package opearatos;

public class RelationalOperator {

	public static void main(String[] args) {
		// Relational operator
		  // <,>,<=,>=,==,!=
		int a=45;
		int b= 77;
		boolean f5 = a!=b;
		System.out.println(f5);
		boolean f4 = a==b;
		System.out.println(f4);
		boolean f3 = b>=a;
		System.out.println(f3);
		boolean f2 = a<=b;
		System.out.println(f2);
		
		boolean f1 = a>b;
		System.out.println(f1);
		boolean f = a<b;
		System.out.println(f);

	}

}
