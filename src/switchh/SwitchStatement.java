package switchh;

public class SwitchStatement {

	public static void main(String[] args) {
		
		int a = 3;
		
		switch(a)
		{
		case 1:
			System.out.println("Inside case 1");
		case 2:
			System.out.println("Inside case 2");
		case 3:
		System.out.println("inside case 3");
		
		default:
			System.out.println("case is not matched");
		}
		

	}

}
